package com.example.natsuyasai.ui;


public class CustomListData {

    private String messageData_;
    private String professionData_;


    public void setMessageData(String message) {
        messageData_ = message;
    }

    public String getMessageData() {
        return messageData_;
    }


    public void setProfessionData(String profession) {
        professionData_ = profession;
    }

    public String getProfessionData() {
        return professionData_;
    }
}